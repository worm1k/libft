#include "libft.h"

void		ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*curr;

	if (!alst || !*alst)
		return ;
	while ((*alst)->next)
	{
		curr = *alst;
		*alst = (*alst)->next;
		(*del)(curr, curr->content_size);
		free(curr);
	}
	*alst = NULL;
	alst = NULL;
}
