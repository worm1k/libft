#include "libft.h"

char	*ft_strtoupper(char *s)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if ('a' <= s[i] && s[i] <= 'z')
			s[i] = s[i] - 'a' + 'A';
		i++;
	}
	return (s);
}
