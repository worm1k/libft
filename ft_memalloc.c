#include "libft.h"

void		*ft_memalloc(size_t size)
{
	void	*res;
	char	*chr;
	size_t	i;

	i = 0;
	res = malloc(size);
	if (!res)
		return (NULL);
	chr = (char *)res;
	while (i < size)
	{
		chr[i] = 0;
		i++;
	}
	return (res);
}
