#include "libft.h"

char		*ft_strnew(size_t size)
{
	char	*res;
	size_t	i;

	i = 0;
	res = (char *)malloc((size + 1) * sizeof(char));
	if (!res)
		return (NULL);
	while (i <= size)
	{
		res[i] = '\0';
		i++;
	}
	return (res);
}
